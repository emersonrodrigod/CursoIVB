/*
 * Controle de Fluxo ou desvio de fluxo
 * IF (se)
 * Else (senão)
 */

var verdadeiro = true;
var falso = false;

/*Se a expressão for verdadeira passa pelo if
 * se a expressão for falsa passa pelo else*/
if (verdadeiro == true && falso != false) {
    alert("Passou pelo IF");
} else {
    alert("Passou pelo Else");
}


